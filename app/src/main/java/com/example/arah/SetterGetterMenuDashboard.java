package com.example.arah;

public class SetterGetterMenuDashboard {

    String tittleMenuDashboard;
    String imgMenuDashboard;

    public SetterGetterMenuDashboard(String tittleMenuDashboard, String imgMenuDashboard) {
        this.tittleMenuDashboard = tittleMenuDashboard;
        this.imgMenuDashboard = imgMenuDashboard;
    }

    public String getTittleMenuDashboard() {
        return tittleMenuDashboard;
    }

    public void setTittleMenuDashboard(String tittleMenuDashboard) {
        this.tittleMenuDashboard = tittleMenuDashboard;
    }

    public String getImgMenuDashboard() {
        return imgMenuDashboard;
    }

    public void setImgMenuDashboard(String imgMenuDashboard) {
        this.imgMenuDashboard = imgMenuDashboard;
    }



}
